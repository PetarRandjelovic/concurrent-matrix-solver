package components.Matrix;

public class MatrixArray {

        public int row;
        public int column;
        public int value;
        public String matrixName;


        public MatrixArray(int row, int column, int value, String matrixName) {
            this.row = row;
            this.column = column;
            this.value = value;
            this.matrixName = matrixName;
        }

}
